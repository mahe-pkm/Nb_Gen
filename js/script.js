function copyFullPrompt(btn, forceDupatta = false) {
    const card = btn.parentElement.parentElement;
    const poseTitle = card.querySelector("strong").textContent;
    const poseText = card.querySelector("textarea").value;

    // Get active tab name to append to notification
    const activeTab = card.querySelector(".tab.active").textContent;

    // Highlight Logic
    document.querySelectorAll('.pose-card').forEach(c => c.classList.remove('copied-pose'));
    card.classList.add('copied-pose');

    const master = document.getElementById("masterPrompt").value;
    const additional = document.getElementById("additionalPrompt").value;
    const neg = document.getElementById("negativePrompt").value;

    // Handle Dupatta
    let dupattaText = "";
    const dupattaStyle = document.querySelector('input[name="dupattaStyle"]:checked').value;

    // Priority: Button Force (forces FULL if nothing selected/none) OR Selection
    // Note: If user clicks "+ Dupatta" button, we force 'full' style if 'none' was selected, 
    // or use selected non-none style.
    let useStyle = dupattaStyle;
    if (forceDupatta && useStyle === 'none') useStyle = 'full';

    const useDupatta = (useStyle !== 'none');

    if (useDupatta) {
        dupattaText = "\n\n{ (DUPATTA PROMPT) }\n" + dupattaPrompts[useStyle];
    }

    const full = `{ (MASTER PROMPT) }\n${master}${dupattaText}\n\n{ (POSE PROMPT) }\n${poseText}\n\n{ (ADDITIONAL PROMPT) }\n${additional}\n\n{ (NEGATIVE PROMPT) }\n${neg}`;

    copyToClipboard(full, () => {
        const toastMsg = useDupatta ? `Copied: "${poseTitle}" (+Dupatta)` : `Copied: "${poseTitle}"`;
        showToast(toastMsg);
    });
}

