function switchVariation(poseId, variationKey) {
    // Get data
    const data = poseData[poseId][variationKey];
    const card = document.getElementById(poseId);

    // Update UI
    card.querySelector("textarea").value = data.text;
    const imgEl = card.querySelector("img");
    imgEl.src = data.img;

    // Update tabs
    const tabs = card.querySelectorAll(".tab");
    tabs.forEach(t => t.classList.remove("active"));

    // Find specific tab index
    const index = parseInt(variationKey.replace('v', '')) - 1;
    if (tabs[index]) {
        tabs[index].classList.add("active");
    }
}

function showToast(message) {
    const container = document.getElementById('toast-container');
    const toast = document.createElement('div');
    toast.className = 'toast';
    toast.textContent = message;
    container.appendChild(toast);

    setTimeout(() => {
        toast.remove();
    }, 3000);
}

